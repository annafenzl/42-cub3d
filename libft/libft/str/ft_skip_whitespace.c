#include "../libft.h"

int	ft_skip_whitespace(char *str)
{
	int	i;

	i = 0;
	if (str == NULL)
		return (-1);
	while (str[i] != '\0' && (str[i] == ' '
			|| str[i] == '\t' || str[i] == '\v'
			|| str[i] == '\b'))
	{
		i++;
	}
	return (i);
}
